    private void btn_showApplicantDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_showApplicantDetailActionPerformed
        ApplicantDetailForm applicantDetail = new ApplicantDetailForm();
        try {
            String applicantName = list_applicantList.getSelectedValue().toString();
            ArrayList<Applicant> applicantSelect = new ArrayList<>();
            for (int i = 0 ; i < applicantArr.size() ; i++) {
                if (applicantName.equals(applicantArr.get(i).name)) {
                    applicantSelect.add(applicantArr.get(i));
                    applicantDetail.setApplicant(applicantName, applicantSelect);
                    applicantDetail.show();
                    break;
                }
            }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "กรุณาเลือกงานที่ต้องการ", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_showApplicantDetailActionPerformed

