    public void setApplicantList(String jobName) {
        DefaultListModel applicantList = new DefaultListModel();
        
        applicantArr = applicantListService.getApplicantListByJobName(jobName);
        
        for (int i = 0 ; i < applicantArr.size() ; i++) {
            applicantList.addElement(applicantArr.get(i).name);
        }
        list_applicantList.setModel(applicantList);
    }

